package JavaExercises.OperatorPrograms;

public class RelationalOperator {
    public static void main(String[] args) {
        int x = 15, y = 10;
        System.out.println(x > y);
        System.out.println(x < y);
        System.out.println(x == y);
        System.out.println(x != y);
        System.out.println(x >= y);
        System.out.println(x <= y);
    }
}
